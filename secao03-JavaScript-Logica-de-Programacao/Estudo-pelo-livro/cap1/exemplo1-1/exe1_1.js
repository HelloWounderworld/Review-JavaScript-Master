// 1.6 Variáveis e constantes

// As variáveis são espaços alocados na memória do computador que
// permitem guardar informações e trabalhar com elas – como o valor que o
// cliente deseja sacar no terminal do caixa eletrônico.
// Para declarar uma variável utilizar a sintaxe "var" em seguida de algum nome em mínúsculo e depois com uma atribuição "=" e, por último, um valor de atribuição.

var idade = 18;

console.log('Variável idade: ', idade);

// Diferente das variáveis, uma constante não pode ter o seu
// conteúdo alterado no decorrer do programa.
// Para declarar uma constante utilizar a sintaxe "const" com a mesma analogia das declarações das variáveis, mas com letras maiúsculas.

const POLTRONAS = 240;

console.log('Constante poltronas: ', POLTRONAS);